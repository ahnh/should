<b id="generalDatasourceAnswer"></b>
<p>
    The data comes from 2 separate open data sources.  Crime related data is retrieved from Toronto Police Services, and housing data is retrieved from the Toronto Real Estate Board.
</p>

<b id="generalDataReliablityAnswer"></b>
<p>
    The Should I Live Here website doesn't manipulate the original data coming into the database. 
    The website simply stores data and displays it to you in an organized and readable manner.
    As the sources of the data are considered reliable in their respective industries, we consider the data to be 100% reliable.   
</p>

<b id="howToHousingDataAnswer"></b>
<p>
    Housing cost "only" is the default view.  If you have clicked either "Display All" in the crime checkbox area, or "Display Only Crime", or a combination of the 2, deslect the appropriate boxes and only housing cost results will be shown.
</p>

<b id="howToCrimeDataAnswer"></b>
<p>
    To view only crime data, scroll down to the bottom of the search form and select the "View Crime Only" checkbox.  Then select which crime types you wish to search for, and the map will populate with all selected crime incidents of the chose type(s).  Use "Display All" to select all crime types.
</p>

<b id="howToGTASectorsAnswer"></b>
<p>
    In the sector dropdown menu, select the sector name you wish to search for.  The map reloads on the fly if you wish to move between different sectors.
</p>

<b id="troubleshootDisplayAnswer"></b>
