<div class="row">
    <h4>Mission Statement</h4>
    <p>
        To create a sustainable product that allows users to search for average housing 
        costs, or rental prices, versus incidents of specific crime types within the Toronto area.
    </p>
</div>

<div class="row purpose">
    <h4>Purpose of our Website</h4>
        <p>
            To allow users to find out average cost of housing, rental or purchasing, and the 
            incidents of specific types of crime, by sectors in the Greater Toronto area.

            At the end of the day we hope our users will find the information useful and a 
            step closer in finding a ideal and safe place to call home.
        </p>
</div>

